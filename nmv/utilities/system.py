# System imports
import os
import subprocess


####################################################################################################
# @command_exists
####################################################################################################
def command_exists(name):
    """Checks if a given command exists or not.

    :param name:
        Command.
    :return:
        True of False.
    """

    try:
        devnull = open(os.devnull)
        subprocess.Popen([name], stdout=devnull, stderr=devnull).communicate()
    except OSError as e:
        return False
    return True
